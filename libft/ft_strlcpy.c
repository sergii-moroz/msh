#include "libft.h"

unsigned long	ft_strlcpy(char *dest, const char *src, unsigned long size)
{
	unsigned long	i;
	unsigned long	src_len;

	i = 0;
	src_len = ft_strlen(src);
	if (size == 0)
		return (src_len);
	while (i < size - 1 && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (src_len);
}
